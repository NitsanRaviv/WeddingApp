		$(function() {  
			
		$("input[type=radio][name='song']").on('change', function() {
			console.log('change');
			$("#btnSubmit").attr("disabled", false);
			$("#btnSubmit").addClass("active");
		});

        $("#btnSubmit").click(function() {  
			$("#btnSubmit").attr("disabled", true);
			$("#btnSubmit").addClass("disable");
			$("#btnSubmit").removeClass("active");
           // validate and process form here
           var radio_button_value;

           if ($("input[name='song']:checked").length > 0){
               radio_button_value = $('input:radio[name=song]:checked').val();
			  
              // alert(radio_button_value)
           }
           else{
               alert("No button selected, try again!");
			   $("#btnSubmit").attr("disabled", false);
			   $("#btnSubmit").removeClass("disable");
			   $("#btnSubmit").addClass("active");
               return false;
           }


           $.ajax({
               type: "POST", 
               url: "submit.php",  
               data:{'song':radio_button_value},  
               success: function() { 
                    // alert("form submitted: "+ radio_button_value);
                    $(".tanks-page").show();
                    $(".section-page").hide();
					$("#btnSubmit").attr("disabled", false);
					$("#btnSubmit").removeClass("disable");
					$("#btnSubmit").addClass("active");
               }
            });
            return false;
         });
});
